package com.gpersist.entity.user;

public class UserAge {
    private String fifties;
    private String sixties;
    private String seventies;
    private String eighties;
    private String nineties;
    private String custom;

    public String getFifties() {
        return fifties;
    }

    public void setFifties(String fifties) {
        this.fifties = fifties;
    }

    public String getSixties() {
        return sixties;
    }

    public void setSixties(String sixties) {
        this.sixties = sixties;
    }

    public String getSeventies() {
        return seventies;
    }

    public void setSeventies(String seventies) {
        this.seventies = seventies;
    }

    public String getEighties() {
        return eighties;
    }

    public void setEighties(String eighties) {
        this.eighties = eighties;
    }

    public String getNineties() {
        return nineties;
    }

    public void setNineties(String nineties) {
        this.nineties = nineties;
    }

    public String getCustom() {
        return custom;
    }

    public void setCustom(String custom) {
        this.custom = custom;
    }

}
